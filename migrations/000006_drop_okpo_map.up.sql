-- ============================================================================
--  000006_drop_okpo_map — упразднение okpo_map в config источника 'lk' (§3.12).
--
--  Идентификация «чей файл»/терминала переехала в справочник ports (по ОКПО
--  грузополучателя; окпо не уникален — у одного ОКПО может быть несколько
--  терминалов). Прежний okpo_map (ОКПО→код порта) был тупиковым. Приём ЛК теперь
--  валидирует ОКПО через ports (DirectoryCache.PortsByOkpo), не через config.
--
--  Идемпотентно: удаление отсутствующего ключа JSONB — no-op.
-- ============================================================================

SET search_path TO dpport;

UPDATE dpport.data_source
   SET config = config - 'okpo_map',
       updated_at = now()
 WHERE id = 'lk';
