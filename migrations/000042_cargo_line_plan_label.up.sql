-- ============================================================================
--  000042_cargo_line_plan_label — привязка линии учёта к колонке плана подвода.
--
--  «Ост на 18 станция» (cargo_work.ost_st) берётся из служебной строки плана
--  «Остаток на 18:00» (plan_nitka.is_ostatok), где числа разложены по ячейкам
--  plan_nitka.ports = [{label, count, is_our}]. Метка ячейки — это заголовок
--  столбца Excel вида «АО "НАХОДКИНСКИЙ МТП" Каменный уголь», то есть
--  организация + род груза, а НЕ имя нашего терминала (ГУТ-2/АЭ/УТ-1).
--
--  В gtport это сопоставление было зашито колонками: ma → Attis/Nmtp/NmtpM/NmtpCh,
--  nk → Nmtp (getOstStFromPlan + GetRemainingValues). Здесь оно становится
--  настройкой: линия учёта знает, из какой колонки плана брать свой остаток.
--
--  Пусто → остаток со станции для линии не подтягивается (0). Сравнение точное,
--  по полной метке: организации в плане пишутся с кавычками и формой
--  собственности, частичный матч дал бы ложные срабатывания на «ПРОЧИЕ ГРУЗЫ».
-- ============================================================================

SET search_path TO dpport;

ALTER TABLE dpport.port_cargo_line
    ADD COLUMN IF NOT EXISTS plan_label text NOT NULL DEFAULT '';

COMMENT ON COLUMN dpport.port_cargo_line.plan_label IS 'Колонка плана подвода (для остатка на станции)';
