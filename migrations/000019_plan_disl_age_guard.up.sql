-- ============================================================================
--  000019_plan_disl_age_guard — порог свежести дислокации для загрузки плана.
--
--  Не даём грузить план подвода на устаревший снимок: если дислокация (по метке
--  формирования из документа ЛК) старше plan_max_disl_age_minutes — загрузка плана
--  блокируется. На устаревшем снимке результат матча/простановки недостоверен.
--
--  Значение владельца — 60 мин. Порог в настройках (не хардкод); ноль/отсутствие
--  → гард выключен. Кладём под ingest_policy.plan рядом с plan_max_lag_hours.
-- ============================================================================

SET search_path TO dpport;

UPDATE dpport.client_settings
   SET ingest_policy = jsonb_set(ingest_policy, '{plan,plan_max_disl_age_minutes}', '60'),
       updated_at = now()
 WHERE id = 1;
