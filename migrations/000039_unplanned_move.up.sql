-- ============================================================================
--  000039_unplanned_move — «бесплановые в подходе» для карточки «Оперативка».
--
--  Сигнал оператору плановой станции: гружёный вагон БЕЗ плановых данных
--  (plan_msk пусто), ближе порога км до станции назначения (настройка
--  client_settings.extra.status.unplanned_move_km, дефолт 1000), СМЕНИЛ станцию
--  операции (движется). Ловится на этапе сравнения снимков (Stage 2).
--
--  Запись живёт до: указания оператора (кнопка «Скрыть»), появления плана у
--  вагона либо его прибытия/порожнего состояния (автоснятие). Повторная смена
--  станции после скрытия создаёт запись заново (сигнал снова актуален).
--
--  Структура = полная копия колонок dislocation (LIKE), ключ — vagon.
-- ============================================================================

SET search_path TO dpport;

CREATE TABLE dpport.unplanned_move (LIKE dpport.dislocation INCLUDING DEFAULTS);
ALTER TABLE dpport.unplanned_move ADD PRIMARY KEY (vagon);
