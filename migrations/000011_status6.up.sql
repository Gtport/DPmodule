--  станции поломки, а marka груз не знает (станция не наш отправитель) — берём
--  данные груза у донора из этой таблицы (матч по станции операции + вес + срок).
--
--  Пишется при ПЕРЕХОДЕ на статус 6 (в новом батче 6, в актуальной ≠6). Хранит
--  полную запись донора (для матча и передачи груза). gruzpol_s/naznach у донора
--  обнуляются («0») — к нам он не доедет, в выборки не попадает. Удаление: после
--  использования донором (S2-3) + админ-очистка «зависших» (created_at/updated_at).
-- ============================================================================

