--  000009_data_loss_90 — временно поднять порог потери данных до 90%.
--
--  Stage 2 отфильтровывает вагоны вне включённых портов → набор для снимка может
--  заметно сокращаться относительно распарсенного. Пока играем с фильтрами,
--  ослабляем гард (был 30% в 000003). Вернуть к рабочему значению — отдельной
--  миграцией, когда фильтры устоятся.
-- ============================================================================

SET search_path TO dpport;

UPDATE dpport.client_settings
   SET ingest_policy = jsonb_set(ingest_policy, '{dislocation,max_data_loss_pct}', '90'),
       updated_at = now()
 WHERE id = 1;
