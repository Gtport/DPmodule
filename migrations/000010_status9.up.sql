-- ============================================================================
--  000010_status9 — таблица кандидатов в прибытие (§3.14, S2-1).
--
--  Вагоны-кандидаты, ожидающие подтверждения прибытия оператором. Два типа
--  (в колонке status):
--    9 — «живой кандидат»: вагон в новом батче на станции назначения, гружёный,
--        date_prib пусто. Остаётся и в снимке dislocation. Удаляем: оператором
--        (отклонение) или авто при смене статуса в след. цикле.
--    8 — «пропавший»: был в актуальной, исчез из батча (статус ≠ 6). В снимок НЕ
--        идёт. Не удаляем через UI (защита от тех.ошибок РЖД): либо оператор
--        подтверждает прибытие, либо вагон снова появится в дислокации → авто-удаление.
--        Наполнение статусом 8 — в S2-1b.
--
--  Структура = полная копия колонок dislocation (LIKE), ключ — vagon (один вагон =
--  один кандидат). created_at/updated_at — для админ-очистки «вечных» статус-8.
--  «Вечные» кандидаты чистит администратор (не оператор), см. §3.14.
-- ============================================================================

SET search_path TO dpport;

CREATE TABLE dpport.status9 (LIKE dpport.dislocation INCLUDING DEFAULTS);
ALTER TABLE dpport.status9 ADD PRIMARY KEY (vagon);
CREATE INDEX ix_status9_status ON dpport.status9 (status);
