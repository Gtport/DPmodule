-- ============================================================================
--  000038_status9_dismissed — пометка «кандидат отклонён оператором».
--
--  Таблица прибывших показывает живых кандидатов статуса 9 (вагон на станции
--  назначения, date_prib из АСУ пуст); оператор подтверждает прибытие вручную
--  либо отклоняет («скрыть до новых данных», решение владельца). Отклонение —
--  пометка на записи-кандидате: вагон остаётся 9, из списка кандидатов скрыт;
--  запись (вместе с пометкой) штатно снимается reconcileCandidates при смене
--  статуса, поэтому «до новых данных» выполняется само.
-- ============================================================================

ALTER TABLE dpport.status9
    ADD COLUMN IF NOT EXISTS dismissed_at timestamp;

COMMENT ON COLUMN dpport.status9.dismissed_at IS
    'Когда оператор отклонил кандидата в прибывшие (скрыт из списка); NULL — не отклонён';
