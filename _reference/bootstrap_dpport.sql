-- ============================================================================
-- bootstrap_dpport.sql
--
-- Создаёт ОТДЕЛЬНУЮ базу под прототип GTport Universal и схему dpport.
-- Прод (gtport_prod) НЕ ТРОГАЕТСЯ.
--
-- Запускать ОДИН РАЗ, вручную, под суперпользователем postgres
-- (CREATE DATABASE нельзя выполнить из миграций приложения — приложение
--  уже подключено к своей базе и не может создать её само):
--
--     psql -U postgres -h localhost -f bootstrap_dpport.sql
--
-- Скрипт идемпотентный: повторный запуск ничего не ломает.
--
-- ИМЕНА (поменяйте здесь, если хотите другие):
--   база приложения  : dpport
--   схема приложения : dpport
--   владелец/роль     : gtport_app   (та же роль, что у прода — уже существует)
--   база тайлов       : gtport_tiles (СУЩЕСТВУЮЩАЯ, переиспользуем — не создаём)
-- ============================================================================


-- 1. Отдельная база приложения -----------------------------------------------
-- CREATE DATABASE ... IF NOT EXISTS в Postgres нет, поэтому guard через \gexec:
SELECT 'CREATE DATABASE dpport OWNER gtport_app'
WHERE NOT EXISTS (SELECT 1 FROM pg_database WHERE datname = 'dpport')\gexec


-- 2. search_path на уровне БАЗЫ ----------------------------------------------
-- Любая новая сессия к dpport по умолчанию работает в схеме dpport.
-- Все CREATE TABLE без явной схемы (миграции, фолбэки init) уедут в dpport.
-- public оставляем вторым — для расширений/общих функций.
ALTER DATABASE dpport SET search_path TO dpport, public;


-- 3. Сама схема (выполняется уже ВНУТРИ базы dpport) --------------------------
\connect dpport

CREATE SCHEMA IF NOT EXISTS dpport AUTHORIZATION gtport_app;

-- Явно выдаём права роли приложения (на случай, если владелец не gtport_app):
GRANT ALL ON SCHEMA dpport TO gtport_app;

-- Проверка: search_path в этой сессии ещё старый (ALTER DATABASE действует
-- со СЛЕДУЮЩЕГО подключения). Это нормально — приложение подключится заново.


-- 4. База тайлов — отдельную НЕ создаём ---------------------------------------
-- Приложение при старте обязательно подключается к БД тайлов (main.go, log.Fatal
-- при неудаче). Переиспользуем существующую рабочую gtport_tiles:
-- в _env форка оставить TILES_DB_NAME=gtport_tiles (роль gtport_app уже имеет
-- к ней доступ). Тайлы — кэш OSM-картинок, не бизнес-данные; делёж с продом
-- безопасен. Поэтому здесь ничего создавать не нужно.


