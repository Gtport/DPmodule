// Package plan разбирает файлы «плана подвода» портов (Excel) в единую доменно-
// независимую структуру: список ниток (плановых прибытий поездов) с временем МСК
// и количеством «наших» вагонов (Activ) — целью последующего сопоставления с
// дислокацией (этап match, отдельный слой).
//
// Ключевая идея унификации: станции — это ДАННЫЕ (профиль), а не код. Один
// generic-парсер (grid.go) читает любой файл «новой формы» плана; специфика
// станции сведена к профилю (profile.go) — какие терминалы считать «нашими».
// Добавление/удаление станции не трогает ядро: правится карта профилей, а при
// действительно ином формате листа — регистрируется свой парсер (registry.go).
package plan

import "time"

// PlanDoc — результат разбора одного файла плана подвода.
type PlanDoc struct {
	PlanCode   string      // код станции плана: «ma», «nk», …
	SourceFile string      // имя исходного файла (для журналирования и БД)
	Nitki      []PlanNitka // нитки (плановые прибытия), в порядке следования в файле
}

// PlanNitka — одна нитка плана: планово прибывающий поезд (или строка с.ф.).
//
// Время храним как naive time.Time (Московский календарь, без таймзоны — канон
// проекта). PlanMsk уже с учётом бизнес-правила «час ≥ 18 → предыдущие сутки».
type PlanNitka struct {
	Index   string    // индекс поезда из столбца «Индекс» (13-значный, как в файле)
	IndexPp string    // нормализованная метка нитки (для обычного поезда = Index)
	PlanMsk time.Time // момент нитки в МСК-календаре (нулевой — если время не разобрано)
	FactMsk time.Time // фактическое прибытие (нулевой — если факта нет)
	Otkl    string    // отклонение «факт − план», формат «±HH:MM» («» если факта нет)
	Wagons  int       // «Кол. ваг.» — всего вагонов в поезде (все терминалы)
	Activ   int       // вагоны «наших» причалов (профиль OurTerminals) — цель скоринга
}
